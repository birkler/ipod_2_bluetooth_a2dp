import time

ser = serial.Serial() # open serial port

ser.port = '/dev/ttyS0'

ser.baudrate = 19200
ser.bytesize = serial.EIGHTBITS
ser.parity = serial.PARITY_NONE
ser.stopbits = serial.STOPBITS_ONE
ser.timeout = 10

ser.open()
print(ser.isOpen)
